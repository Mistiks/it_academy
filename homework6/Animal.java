package homeworks.homework6;

public class Animal {
    private int age;
    private String nick;

    public Animal(int age, String nick) {
        this.age = age;
        this.nick = nick;
    }

    public int getAge() {
        return this.age;
    }

    public String getNick() {
        return this.nick;
    }

    public String getInfo() {
        return "Nick: " + getNick() + "\t" + " Age: " + getAge();
    }
}